@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly languages: LanguageOption[] = [
    { locale: 'en-US', code: 'US', label: 'English' },
    { locale: 'pt-PT', code: 'PT', label: 'Português' },
    { locale: 'pt-BR', code: 'BR', label: 'Português Brasil' },
    { locale: 'es-ES', code: 'ES', label: 'Español' }
  ];

  readonly currentLocale = signal<SupportedLocale>(this.getInitialLocale());

  setLocale(locale: SupportedLocale): void {
    this.currentLocale.set(locale);
    localStorage.setItem('music-school-locale', locale);
    document.documentElement.lang = locale;
  }

  translate(key: string): string {
    return translations[this.currentLocale()][key] ?? translations['en-US'][key] ?? key;
  }

  private getInitialLocale(): SupportedLocale {
    const storedLocale = localStorage.getItem('music-school-locale');
    return this.languages.some((language) => language.locale === storedLocale)
      ? storedLocale as SupportedLocale
      : 'pt-PT';
  }
}
